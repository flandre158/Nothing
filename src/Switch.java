public class Switch {
    public static void main(String[] args) {
        char a = '0';
        switch (a) {
            case '+': {
                System.out.println("+");
                break;
            }
            case '-': {
                System.out.println("-");
                break;
            }
            default: {
                System.out.println("fuck you the way");
            }
        }
    }
}
